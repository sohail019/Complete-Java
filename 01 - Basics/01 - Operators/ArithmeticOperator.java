class ArithmeticOperator{
	public static void main(String[] args){
		int a = 10;
		int b = 20;
		
		int addRes = a + b;
		int subtractRes = a - b;
		int multiplyRes = a * b;
		int divideRes = a / b;
		int modulusRes = a % b;
		
		System.out.println(addRes);
		System.out.println(subtractRes);
		System.out.println(multiplyRes);
		System.out.println(divideRes);
		System.out.println(modulusRes);
	}
}