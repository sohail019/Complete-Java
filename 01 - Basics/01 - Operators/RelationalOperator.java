class RelationalOperator{
	public static void main(String[] args){
		int a = 10;
		int b = 5;
		
		// Lesser than
		System.out.println(a < b); // false
		
		// Greater than
		System.out.println(a > b); // true

		// Lesser than equal to
		System.out.println(a <= b); // false
		
		// Greater than equal to
		System.out.println(a >= b); // true

		// Equal to	
		System.out.println(a == b); // false
		
		
		// Not equal to
		System.out.println(a != b); // true
	}
}