class TernaryEx2{
    // WAP to print positive if num is Positive number, otherwise print Negative if number is negative number
    public static void main(String[] args){
        int a = 5;
        int b = -5;

        System.out.println(a > 0 ? "Positive" : "Negative"); // Positive
        System.out.println(b > 0 ? "Positive" : "Negative"); // Negative
    }
}