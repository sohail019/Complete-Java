class TernaryEx3{
    // WAP to print Even if num is Even number, otherwise print Odd if number is Odd number
    public static void main(String[] args){
        int a = 10;
        int b = 11;

        System.out.println(a % 2 == 0 ? "Even" : "Odd"); // Even
        System.out.println(b % 2 == 0 ? "Even" : "Odd"); // Odd
    }
}